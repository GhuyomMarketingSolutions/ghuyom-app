import 'package:flutter/material.dart';

class TextStyleUtil {
  static TextStyle manrope400({
    Color color = Colors.black,
    required double fontSize,
  }) {
    return TextStyle(
      fontFamily: 'Manrope',
      color: color,
      fontSize: fontSize,
      fontWeight: FontWeight.w400,
    );
  }

